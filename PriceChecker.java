    // if we use the long constructor, default to cache true. Ensure the timeout is above 1 ms.. idk why anyone would do that but whatever.
    public PriceChecker(long cacheTimeout) {
        this.initItemIdHashMap();
        startTime = System.currentTimeMillis();
        this.cache = new HashMap<>();
        if(this.cacheTimeout < 1) {
            log("Cache timeout is too low, defaulting to 10 minutes.");
            this.cacheTimeout = TEN_MINUTES;
        } else {
            this.cacheTimeout = cacheTimeout;
        }
    }

    /**
     * If caching is enabled, clears the cache so that new results are fetched on lookup.
     */
    public void flushCache() {
        if(cache != null) {
            cache.clear();
        }
    }

    public int getOurItemsWorthTotal(String itemName) {
        return this.getOurItemsWorthFromInventory(itemName) + this.getOurItemsWorthFromEquipment(itemName) + this.getOurItemsWorthFromBank(itemName);
    }

    public int getOurItemsWorthFromInventory(String itemName) {
        if(Inventory.contains(itemName)) {
            GELookupResult r = this.lookup(itemName);
            if(r == null) return 0;
            return Inventory.count(itemName) * r.price;
        } else {
            return 0;
        }
    }

    public int getOurItemsWorthFromEquipment(String itemName) {
        Item[] items = Equipment.all().stream().toArray(Item[]::new);

        if(items.length <= 0) {
            return 0;
        } else {
            for(int i = 0; i < items.length; i++) {
                Item item = items[i];
                if(item != null && item.getName().equals(itemName)) {
                    int amount = 1;
                    try {
                        amount = item.getAmount();
                        if(amount <= 0) {
                            amount = 1;
                        }
                    } catch(Exception e) {
                        log("amt Error:" + e);
                        amount = 1;
                    }
                    return amount * this.lookupPrice(item.getID());
                }
            }
        }
        return 0;
    }

    public int getOurItemsWorthFromBank(String itemName) {
        int c = 0;
        if(Bank.isOpen()) {
            c = Bank.count(itemName);
        } else {
            if(Bank.getBankHistoryCache() != null) {
                for(int i = 0; i < Bank.getBankHistoryCache().size(); i++) {
                    if(Bank.getBankHistoryCache().get(i) != null && Bank.getBankHistoryCache().get(i).getName().equals(itemName)) {
                        c = Bank.getBankHistoryCache().get(i).getAmount();
                        break;
                    }
                }
            }
        }
        if(c == 0) return 0;
        GELookupResult r = this.lookup(itemName);
        if(r == null) return 0;
        return c * r.price;
    }

    public GELookupResult lookup(String itemName) {
        List<Integer> ids = this.itemIdHashMap.get(itemName);
        if(ids == null) return null;
        if(ids.size() > 0) {
            int i;
            for(i = 0; i < ids.size(); i++) {
                GELookupResult res = this.lookup(ids.get(i));
                if(res != null) {
                    return res;
                }
            }
            return null;
        } else {
            return null;
        }
    }

    public int lookupPrice(String itemName) {
        GELookupResult r = this.lookup(itemName);
        if(r != null) {
            return r.price;
        } else {
            return -1;
        }
    }

    public int lookupPrice(int itemId) {
        GELookupResult r = this.lookup(itemId);
        if(r != null) {
            return r.price;
        } else {
            return -1;
        }
    }

    /**
     * Looks up an item using the Grand Exchange API. This method blocks while waiting for the API result.
     * @[member='param'] itemId the id to look up.
     * @return the result returned by the api. May be null if an error has occurred.
     */
    public GELookupResult lookup(int itemId) {
        if((System.currentTimeMillis() - this.cacheTimeout) > startTime){ //Flush cache after 10 minutes. Auto-update prices.
            flushCache();
            startTime = System.currentTimeMillis();
        }

        if(cache != null && !cache.isEmpty()) {
            GELookupResult result = cache.get(itemId);
            if(result != null) {
                return result;
            }
        }

        String json;
        try {
            String formatted = String.format(API_LOCATION, itemId);
            URL url = new URL(formatted);
            Scanner scan = new Scanner(url.openStream()).useDelimiter("\\A");
            json = scan.next();
            //   log("json was" + json);
            scan.close();
        } catch(IOException e) {
            return null;
        }

        GELookupResult result = parse(itemId, json);
        if(result != null)  {
            //   log("item was" + result.name + result.price);
        }

        if(cache != null) {
            cache.put(itemId, result);
        }

        return result;
    }

    /**
     * Parses a GELookupResult from the JSON returned by the API.
     * @[member='param'] itemId The item ID.
     * @[member='param'] json The JSON returned by the RuneScape's API.
     * @return The serialized result.
     */
    private static GELookupResult parse(int itemId, String json) {
        JsonObject obj = JsonParser.parseString(json).getAsJsonObject();
        if(obj == null) {
            return null;
        }
        JsonObject item = obj.getAsJsonObject("item");
        if(item == null) {
            return null;
        }
        //    log("item json object:" + item);
        JsonObject current = item.getAsJsonObject("current");
        //  log("Current:"+ current);
        int price = 0;
        String p = current.get("price").getAsString();
        try {
            int kIndex = p.indexOf("k");
            int mIndex = p.indexOf("m");
            if(kIndex > 0) {
                price = (int) (NumberFormat.getInstance().parse((p.substring(0, p.length()-1)).replace(",", "")).doubleValue() * 1000);
            } else if(mIndex > 0) {
                price = (int) (NumberFormat.getInstance().parse((p.substring(0, p.length()-1)).replace(",", "")).doubleValue() * 1000000);
            } else {
                p = p.replace(",", "");
                price = NumberFormat.getInstance().parse(p).intValue();
            }
        } catch (Exception e) {
            log("Error parsing price...");
            log(e.getMessage());
        }
        return new GELookupResult(
                item.get("icon").getAsString(),
                item.get("icon_large").getAsString(),
                item.get("type").getAsString(),
                item.get("typeIcon").getAsString(),
                item.get("name").getAsString(),
                item.get("description").getAsString(),
                Boolean.parseBoolean(item.get("members").getAsString()),
                itemId,
                price
        );
    }

